    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        int row = tblKhachHang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 dòng trên table để update");
        } else {
            KhachHang x = getKhachHang();
            Integer maKhachHang = Integer.valueOf(txtMaKhachHang.getText());
            JOptionPane.showMessageDialog(this, serviceKH.suaKH(x, maKhachHang));
            listKH = serviceKH.listKH();
            loadTable(listKH);
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaKHActionPerformed
        int row = tblKhachHang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 dòng trên table để xoa");
        } else {
            int temp = JOptionPane.showConfirmDialog(this, "Ban co muon xoa khong ?");
            if (temp == JOptionPane.OK_OPTION) {
                Integer maKhachHang = Integer.valueOf(txtMaKhachHang.getText());
                JOptionPane.showMessageDialog(this, serviceKH.xoaKH(maKhachHang));
                listKH = serviceKH.listKH();
                loadTable(listKH);
            }
        }
    }//GEN-LAST:event_btnXoaKHActionPerformed


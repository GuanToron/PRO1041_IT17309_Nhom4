    private void loadTable(ArrayList<KhachHang> list) {
        modelKH.setRowCount(0);
        modelKH.setColumnIdentifiers(new String[]{"Mã khách hàng", "Tên khách hàng", "Giới tính", "Ngày sinh, Địa chỉ", "SDT", "Điểm tích luỹ"});
        for (KhachHang x : list) {
            modelKH.addRow(x.toDataRow());
        }
    }

    private KhachHang getKhachHang() {
        KhachHang x = new KhachHang();
        x.setDiaChi(txtDiaChi.getText());
        x.setDiemTichLuy(Integer.valueOf(txtDiemTichLuy.getText()));
        Integer gioiTinh = 0;
        if (rdNam.isSelected()) {
            gioiTinh = 1;
        }
        if (rdNu.isSelected()) {
            gioiTinh = 0;
        }
        x.setGioiTinh(gioiTinh);
        String ngaySinh = txtNgaySinh.getText();
        Date d = null;
        try {
            d = new SimpleDateFormat("yyyy-dd-mm").parse(ngaySinh);
        } catch (Exception e) {
            e.printStackTrace();
        }
        x.setNgaySinh(d);
        x.setSdt(txtSDT.getText());
        x.setTenKH(txtTenKhachHang.getText());
        return x;
    }
    
    private void loadTextField(int i){
        KhachHang x = listKH.get(i);
        txtDiaChi.setText(String.valueOf(x.getDiaChi()));
        txtDiemTichLuy.setText(String.valueOf(x.getDiemTichLuy()));
        txtMaKhachHang.setText(String.valueOf(x.getMaKH()));
        txtNgaySinh.setText(String.valueOf(x.getNgaySinh()));
        txtSDT.setText(String.valueOf(x.getSdt()));
        txtTenKhachHang.setText(String.valueOf(x.getTenKH()));
        if(x.getGioiTinh()==1){
            rdNam.setSelected(true);
        }else{
            rdNu.setSelected(true);
        }
    }

